int sum( int num1, int num2 )
{
	int result = num1 + num2;
	return result;
}
int sub( int num1, int num2 )
{
	int result = num1 - num2;
	return result;
}
int multiplication( int num1, int num2 )
{
	int result = num1 * num2;
	return result;
}
int division( int num1, int num2 )
{
	int result = num1 / num2;
	return result;
}
